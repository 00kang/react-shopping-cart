import { rest } from "msw";

const BASE_URL = process.env.REACT_APP_API_URL;
export const getProductsByPage = rest.get(
  `${BASE_URL}/products`,
  (req, res, ctx) => {
    const page = Number(req.url.searchParams.get("_page")) - 1;
    const limit = Number(req.url.searchParams.get("_limit"));
    const targetId = req.url.searchParams.get("id");

    if (targetId) {
      return res(
        ctx.status(200),
        ctx.json(
          productList.filter((product) => product.id === Number(targetId))
        )
      );
    }
    return res(
      ctx.status(200),
      ctx.json(productList.slice(page * limit, (page + 1) * limit))
    );
  }
);

